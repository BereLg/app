			<h4>Etapas del diseño Web </h4>
			<p>Para el diseño de páginas web debemos tener en cuenta tres etapas:</P>
			<h4>El diseño visual de la información que se desea editar</h4>
			<p> En esta etapa se trabaja distribuyendo el texto, los gráficos, los vínculos a otros documentos y otros objetos  multimedia que se consideren pertinentes. Es importante que antes de 'escribir' la página web se realice un boceto o prediseño. Esto facilitará tener un orden claro sobre el diseño.</p>
