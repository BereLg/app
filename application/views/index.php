			<h3>Sitios Web dinámicos y Estáticos</h3>
			<p class="text-justify">Los sitios Web dinámicos son aquellos que permiten crear aplicaciones dentro de la propia Web,
				otorgando una mayor interactividad con el navegante.
				Aplicaciones dinámicas como encuestas y votaciones, foros de soporte, libros de visita, envío de e-mails inteligentes, reserva de productos, pedidos on-line, atención al cliente personalizada.</p>
			<p>Una <strong><ins>página Web estática</ins></strong> es un documento HTML almacenado en el servidor Web
				y no sufre cambios en respuesta a la solicitud de un usuario</p>
